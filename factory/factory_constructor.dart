
/*
 Factory Constructor
  - A factory constructor gives more flexibility when creating objects.
   - It is used to return an instance of a class.
    - this keyword cannot be used in a factory constructor.
    
    

    syntax
    - class ClassName {
        factory ClassName() {
          return ClassName();
        }

        factory ClassName.name() {
          return ClassName();
        }
      }
*/